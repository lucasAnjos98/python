'''
Escreva um programa que leia um medida em metro e converta para centimetros e milimetros
'''

metros = float(input("Informe um media em metros: "))

centimetros = metros * 10
milimetros = metros * 100

print(f"Medida em metros: {metros}")
print(f"Medida em centimetros: {centimetros}")
print(f"Medida em milimetros: {milimetros}")



